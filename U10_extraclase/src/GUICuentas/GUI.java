    private void btnSearchNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchNumberActionPerformed

        try {
            ResultSet rs = CuentaData.buscarCliente(this.inputID.getText());
            if (rs.next()) {
                this.cleanFields();
                this.inputID.setText(rs.getString("id"));
                this.inputOpenningDate.setText(rs.getString("openningDate"));
                this.combType.setSelectedItem(rs.getString("type"));
                this.active = rs.getBoolean("active");
                this.textActive.setText(rs.getBoolean("active") ? "Active" : "Desactive");
                this.inputCId.setText(rs.getString("holderId"));

                this.nuevo = false;
                this.oldID = this.inputID.getText();
                // enable fields
                this.inputOpenningDate.setEnabled(true);
                this.combType.setEnabled(true);
                this.inputCId.setEnabled(true);
                this.btnSearchOwner.setEnabled(true);
                this.btnAdd.setEnabled(true);
                this.btnActivationDesactivation.setEnabled(true);
                this.btnSave.setEnabled(true);
                this.btnSearchNumber.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Not found");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error, while searching");
        }
    }//GEN-LAST:event_btnSearchNumberActionPerformed

