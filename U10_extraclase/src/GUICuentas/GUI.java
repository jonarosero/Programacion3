    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!this.validFields()) {
            JOptionPane.showMessageDialog(null, "Algunos compos necesitan ser llenados");
            return;
        }
        // Create new account and send it to the db
        Cuenta account = new Cuenta();
        account.setId(this.inputID.getText());
        account.setOpenningDate(this.inputOpenningDate.getText());
        account.setType(this.combType.getSelectedItem().toString());
        account.setActive(this.active);

        try {
            if (this.nuevo) {
                CuentaData.nuevoCliente(account, Integer.valueOf(this.inputCId.getText()));
            } else {
                CuentaData.modificarCliente(account, Integer.valueOf(this.inputCId.getText()), this.oldID);
            }
            this.cleanFields();
            this.desactivateFields();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error saving");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

